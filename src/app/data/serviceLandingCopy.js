export function defaultPestLanding(service, pest) {
  const label = pest.slug.startsWith("ot-")
    ? `${service.name.toLowerCase()} от ${pest.name.toLowerCase()}`
    : `${service.name} — ${pest.name}`;
  return {
    metaDescription: `${label} в Москве и МО. Выезд, договор, подбор препарата под объект.`,
    intro: [
      `Выполняем ${label} в Москве и Московской области. Перед выездом уточняем площадь, материалы отделки и ограничения по срокам.`,
    ],
    sections: [
      {
        title: "Как проходит работа",
        body: [
          "Осмотр, согласование метода и препарата, обработка, рекомендации по проветриванию и профилактике.",
        ],
      },
    ],
    faq: faqBlock(warrantyFaq),
  };
}

export function resolvePestLanding(service, pest) {
  return getPestLandingBundle(service.slug, pest.slug) || defaultPestLanding(service, pest);
}
